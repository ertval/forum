// TestSQLiteUserRepositoryIDHandling verifies that repository creates and uses both internal and public IDs
func TestSQLiteUserRepositoryIDHandling(t *testing.T) {
	db, err := sql.Open("sqlite3", ":memory:")
	if err != nil {
		t.Fatalf("Failed to open database: %v", err)
	}
	defer db.Close()

	// Create the users table with proper schema (INT PRIMARY KEY + TEXT public_id)
	_, err = db.Exec(`CREATE TABLE users (
		id INTEGER PRIMARY KEY AUTOINCREMENT,
		public_id TEXT UNIQUE NOT NULL,
		email TEXT UNIQUE,
		username TEXT UNIQUE,
		password_hash TEXT,
		role TEXT,
		post_count INTEGER NOT NULL DEFAULT 0,
		comment_count INTEGER NOT NULL DEFAULT 0,
		created_at TIMESTAMP,
		updated_at TIMESTAMP,
		is_active INTEGER DEFAULT 1
	)`)
	if err != nil {
		t.Fatalf("Failed to create table: %v", err)
	}

	repo := adapters.NewSQLiteUserRepository(db)

	// Create a user
	now := time.Now()
	user := &domain.User{
		Email:        "test@example.com",
		Username:     "testuser",
		PasswordHash: "hashed_password",
		Role:         domain.RoleUser,
		CreatedAt:    now,
		UpdatedAt:    now,
		IsActive:     true,
	}

	ctx := context.Background()
	err = repo.Create(ctx, user)
	if err != nil {
		t.Fatalf("Create returned error: %v", err)
	}

	// Verify that both internal ID and public ID were set
	if user.ID == 0 {
		t.Error("Expected internal ID to be set by repository")
	}
	if user.PublicID == "" {
		t.Error("Expected public ID to be generated by repository")
	}

	// Verify the user was stored with both IDs
	var storedID int
	var storedPublicID string
	err = db.QueryRow("SELECT id, public_id FROM users WHERE email = ?", user.Email).Scan(&storedID, &storedPublicID)
	if err != nil {
		t.Fatalf("Failed to query stored user: %v", err)
	}

	if storedID != user.ID {
		t.Errorf("Expected stored ID %d, got %d", user.ID, storedID)
	}
	if storedPublicID != user.PublicID {
		t.Errorf("Expected stored PublicID %s, got %s", user.PublicID, storedPublicID)
	}

	// Verify that GetByID returns both IDs correctly
	result, err := repo.GetByID(ctx, user.ID)
	if err != nil {
		t.Fatalf("GetByID returned error: %v", err)
	}

	if result.ID != user.ID {
		t.Errorf("Expected ID %d, got %d", user.ID, result.ID)
	}
	if result.PublicID != user.PublicID {
		t.Errorf("Expected PublicID %s, got %s", user.PublicID, result.PublicID)
	}
}

